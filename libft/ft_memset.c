#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*src;

	i = 0;
	src = (char*)s;
	while (n > 0)
	{
		src[i] = c;
		i++;
		n--;
	}
	return (src);
}
