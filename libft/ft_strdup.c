#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*d;

	i = ft_strlen(s1);
	d = (char*)malloc(sizeof(char) * i + 1);
	if (d == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		d[i] = s1[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
